function Menubar() {
  const classes = useStyles();
  const [anchorEl, setAnchorEl] = React.useState(null);
  const [mobileMoreAnchorEl, setMobileMoreAnchorEl] = React.useState(null);
  const [open, setOpen] = React.useState(false);

  const isMenuOpen = Boolean(anchorEl);
  const isMobileMenuOpen = Boolean(mobileMoreAnchorEl);
  const theme = useTheme();

  const handleDrawerOpen = () => {
    setOpen(true);
  };

  const handleDrawerClose = () => {
    setOpen(false);
  };

  const handleProfileMenuOpen = (event) => {
    setAnchorEl(event.currentTarget);
  };

  function handleCamClick(event) {
    setAnchorEl(event.currentTarget);
  }

  const handleMobileMenuClose = () => {
    setMobileMoreAnchorEl(null);
  };

  const handleMenuClose = () => {
    setAnchorEl(null);
    handleMobileMenuClose();
  };

  const handleMobileMenuOpen = (event) => {
    setMobileMoreAnchorEl(event.currentTarget);
  };

  const menuId = 'primary-search-account-menu';
  const renderMenu = (
    <Menu
      anchorEl={anchorEl}
      anchorOrigin={{ vertical: 'top', horizontal: 'right' }}
      id={menuId}
      keepMounted
      transformOrigin={{ vertical: 'top', horizontal: 'right' }}
      open={isMenuOpen}
      onClose={handleMenuClose}
    >
      <MenuItem onClick={handleMenuClose}>Profile</MenuItem>
      <MenuItem onClick={handleMenuClose}>My account</MenuItem>
    </Menu>
  );

  const mobileMenuId = 'primary-search-account-menu-mobile';
  const renderMobileMenu = (
    <Menu
      anchorEl={mobileMoreAnchorEl}
      anchorOrigin={{ vertical: 'top', horizontal: 'right' }}
      id={mobileMenuId}
      keepMounted
      transformOrigin={{ vertical: 'top', horizontal: 'right' }}
      open={isMobileMenuOpen}
      onClose={handleMobileMenuClose}
    >
      <MenuItem>
        <IconButton aria-label="show 11 new notifications" color="inherit">
          <Badge badgeContent={11} color="secondary">
            <NotificationsIcon />
          </Badge>
        </IconButton>
        <p>Notifications</p>
      </MenuItem>
      <MenuItem onClick={handleProfileMenuOpen}>
        <IconButton
          aria-label="account of current user"
          aria-controls="primary-search-account-menu"
          aria-haspopup="true"
          color="inherit"
        >
          <AccountCircle />
        </IconButton>
        <p>Profile</p>
      </MenuItem>
    </Menu>
  );

  return (
    <div className={classes.grow}>
      <AppBar
        position="static"
        className={clsx(classes.appBar, {
          [classes.appBarShift]: open,
        })}
      >
        <Toolbar>
        <IconButton
            color="inherit"
            aria-label="open drawer"
            onClick={handleDrawerOpen}
            edge="start"
            className={clsx(classes.menuButton, open && classes.hide)}
          >
            <MenuIcon />
          </IconButton>
          {/* <Typography className={classes.title} variant="h6" noWrap>
            VICS
          </Typography> */}

          <Button className={classes.menuButton} variant="h6" component={Link} to="/" color="inherit" id="home" style={{ fontSize: '1.25rem' }} >
           Home
          </Button>

          <Button className={classes.menuButton} variant="h6" component={Link} to="/view" color="inherit" id="view" style={{ fontSize: '1.25rem' }} >
           View
          </Button>

          <Button className={classes.menuButton} variant="h6" component={Link} to="/camera" color="inherit" id="camera" style={{ fontSize: '1.25rem' }} onClick={handleCamClick} >
           Camera
          </Button>

          <Button className={classes.menuButton} variant="h6" component={Link} to="/system" color="inherit" id="system" style={{ fontSize: '1.25rem' }} >
           System
          </Button>

          <Button className={classes.menuButton} variant="h6" component={Link} to="/onvif" color="inherit" id="onvif" style={{ fontSize: '1.25rem' }} >
           Onvif
          </Button>

          <Button className={classes.menuButton} variant="h6" component={Link} to="/vics" color="inherit" id="vics" style={{ fontSize: '1.25rem' }} >
           Vics
          </Button>

          <div className={classes.grow} />
          <div className={classes.sectionDesktop}>
            <IconButton aria-label="show 4 new mails" color="inherit">
              <Badge badgeContent={4} color="secondary">
                <MailIcon />
              </Badge>
            </IconButton>
            <IconButton aria-label="show 17 new notifications" color="inherit">
              <Badge badgeContent={17} color="secondary">
                <NotificationsIcon />
              </Badge>
            </IconButton>
            <IconButton
              edge="end"
              aria-label="account of current user"
              aria-controls={menuId}
              aria-haspopup="true"
              onClick={handleProfileMenuOpen}
              color="inherit"
            >
              <AccountCircle />
            </IconButton>
          </div>
          <div className={classes.sectionMobile}>
            <IconButton
              aria-label="show more"
              aria-controls={mobileMenuId}
              aria-haspopup="true"
              onClick={handleMobileMenuOpen}
              color="inherit"
            >
              <MoreIcon />
            </IconButton>
          </div>
        </Toolbar>
      </AppBar>

      <Drawer
        className={classes.drawer}
        variant="persistent"
        anchor="left"
        open={open}
        classes={{
          paper: classes.drawerPaper,
        }}
      >
        <div className={classes.drawerHeader}>
          <IconButton onClick={handleDrawerClose}>
            {theme.direction === 'ltr' ? <ChevronLeftIcon /> : <ChevronRightIcon />}
          </IconButton>
        </div>
        <Divider />
        <List>
            <ListItem button key="home" component={Link} to="/" >
            <ListItemIcon> <HomeIcon /></ListItemIcon>
              <ListItemText  primary="Home" />
            </ListItem>
            <ListItem button key="view" component={Link} to="/view" >
            <ListItemIcon> <AppsIcon /></ListItemIcon>
              <ListItemText  primary="View" />
            </ListItem>
        </List>
        <Divider />
        <List>
            <ListItem button key="camera" component={Link} to="/camera" >
            <ListItemIcon> <CameraAltIcon /></ListItemIcon>
              <ListItemText  primary="Camera" />
            </ListItem>
            <ListItem button key="system" component={Link} to="/system" >
            <ListItemIcon> <SettingsSystemDaydreamIcon /></ListItemIcon>
              <ListItemText  primary="System" />
            </ListItem>
            <ListItem button key="onvif" component={Link} to="/onvif" >
            <ListItemIcon> <SearchIcon /></ListItemIcon>
              <ListItemText  primary="Onvif" />
            </ListItem>
            <ListItem button key="vics" component={Link} to="/vics" >
            <ListItemIcon> <CameraAltIcon /></ListItemIcon>
              <ListItemText  primary="Vics" />
            </ListItem>
        </List>
      </Drawer>
      {renderMobileMenu}
      {renderMenu}
    </div>
  );
}

export default Menubar;
